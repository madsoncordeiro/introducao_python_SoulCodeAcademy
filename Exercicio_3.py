'''
EXERCÍCIO PROPOSTO

1. Crie uma variável número e atribua o valor 15 a ela.
2. Crie um comando de condição para verificar se ela é == a 15, menor que 15, maior que 15.
3. Informe, através de print, a informação para o usuário.
'''

# Criando a variável número e atribuindo o seu valor.
numero = 15

# Criando condicional para verificar se a variável tem valor igual, menor ou maior do que 15.
if numero == 15:
    print("Esse número é igual a 15. ")
elif numero < 15:
    print("Esse número é menor do que 15. ")
else:
    print("Esse número é maior do que 15. ")